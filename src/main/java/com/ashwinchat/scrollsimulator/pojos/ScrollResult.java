package com.ashwinchat.scrollsimulator.pojos;

public class ScrollResult {
    private double cssUsed;
    private double innoUsed;
    private double scrollUsed;

    public double getCssUsed() {
        return cssUsed;
    }

    public void setCssUsed(double cssUsed) {
        this.cssUsed = cssUsed;
    }

    public double getInnoUsed() {
        return innoUsed;
    }

    public void setInnoUsed(double innoUsed) {
        this.innoUsed = innoUsed;
    }

    public double getScrollUsed() {
        return scrollUsed;
    }

    public void setScrollUsed(double scrollUsed) {
        this.scrollUsed = scrollUsed;
    }
}
